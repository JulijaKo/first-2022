package hw1;

public class Bed {
    private String form;
    private double width;
    private String material;
    private String materialColor;
    private String mattressQuality;
    private String transformationMechanism;

    public String getForm() {
        return form;
    }

    public void setForm(String form) {
        this.form = form;
    }

    public double getWidth() {
        return width;
    }

    public void setWidth(double width) {
        this.width = width;
    }

    public String getMaterial() {
        return material;
    }

    public void setMaterial(String material) {
        this.material = material;
    }

    public String getMaterialColor() {
        return materialColor;
    }

    public void setMaterialColor(String materialColor) {
        this.materialColor = materialColor;
    }

    public String getMattressQuality() {
        return mattressQuality;
    }

    public void setMattressQuality(String mattressQuality) {
        this.mattressQuality = mattressQuality;
    }

    public String getTransformationMechanism() {
        return transformationMechanism;
    }

    public void setTransformationMechanism(String transformationMechanism) {
        this.transformationMechanism = transformationMechanism;
    }
}
