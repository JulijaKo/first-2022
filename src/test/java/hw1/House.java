package hw1;

public class House {
    private String address;
    private int numberOfFloors;
    private int numberOfFlats;
    private String location;
    private int putIntoOperation;

    public String getAddress() {
        return address;
    }

    public void setAddress(String address) {
        this.address = address;
    }

    public int getNumberOfFloors() {
        return numberOfFloors;
    }

    public void setNumberOfFloors(int numberOfFloors) {
        this.numberOfFloors = numberOfFloors;
    }

    public int getNumberOfFlats() {
        return numberOfFlats;
    }

    public void setNumberOfFlats(int numberOfFlats) {
        this.numberOfFlats = numberOfFlats;
    }

    public String getLocation() {
        return location;
    }

    public void setLocation(String location) {
        this.location = location;
    }

    public int getPutIntoOperation() {
        return putIntoOperation;
    }

    public void setPutIntoOperation(int putIntoOperation) {
        this.putIntoOperation = putIntoOperation;
    }
}
