package hw1;

public class BadLinenSet {
    private String material;
    private String style;
    private String duvetCoverSize;
    private int numberOfPieces;
    private String color;
    private char empty;

    public String getMaterial() {
        return material;
    }

    public void setMaterial(String material) {
        this.material = material;
    }

    public String getStyle() {
        return style;
    }

    public void setStyle(String style) {
        this.style = style;
    }

    public String getDuvetCoverSize() {
        return duvetCoverSize;
    }

    public void setDuvetCoverSize(String duvetCoverSize) {
        this.duvetCoverSize = duvetCoverSize;
    }

    public int getNumberOfPieces() {
        return numberOfPieces;
    }

    public void setNumberOfPieces(int numberOfPieces) {
        this.numberOfPieces = numberOfPieces;
    }

    public String getColor() {
        return color;
    }

    public void setColor(String color) {
        this.color = color;
    }

    public char getEmpty() {
        return empty;
    }

    public void setEmpty(char empty) {
        this.empty = empty;
    }
}