package hw1;

public class RosesInAVase {
    private int amount;
    private String color;
    private String sort;
    private String vaseForm;
    private String vaseMaterial;

    public int getAmount() {
        return amount;
    }

    public void setAmount(int amount) {
        this.amount = amount;
    }

    public String getColor() {
        return color;
    }

    public void setColor(String color) {
        this.color = color;
    }

    public String getSort() {
        return sort;
    }

    public void setSort(String sort) {
        this.sort = sort;
    }

    public String getVaseForm() {
        return vaseForm;
    }

    public void setVaseForm(String vaseForm) {
        this.vaseForm = vaseForm;
    }

    public String getVaseMaterial() {
        return vaseMaterial;
    }

    public void setVaseMaterial(String vaseMaterial) {
        this.vaseMaterial = vaseMaterial;
    }
}
